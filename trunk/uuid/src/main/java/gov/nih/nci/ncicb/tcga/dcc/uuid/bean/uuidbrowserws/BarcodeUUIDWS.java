package gov.nih.nci.ncicb.tcga.dcc.uuid.bean.uuidbrowserws;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * barcode bean class to be used for the uuid browser view web service
 *
 * @author Dominique Berton
 *         Last updated by: $Author$
 * @version $Rev$
 */

@XmlRootElement(name="barcode")
@XmlAccessorType(XmlAccessType.FIELD)
public class BarcodeUUIDWS {

    @XmlAttribute
    private String createdOn;
    private String barcode;

    public BarcodeUUIDWS() {
    }

    public BarcodeUUIDWS(String createdOn, String barcode) {
        this.createdOn = createdOn;
        this.barcode = barcode;
    }

    public String getCreatedOn() {
        return createdOn;
    }

    public void setCreatedOn(String createdOn) {
        this.createdOn = createdOn;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }
    
}//End of Class
